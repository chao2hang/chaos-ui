#!/usr/bin/env node
/**
 * generate-mdx-map.mjs
 *
 * Recursively scans apps/docs/@/content for all .mdx files and generates
 * apps/docs/@/components/mdx-loaders.ts — a static import map so
 * webpack/turbopack can precisely trace every MDX import rather than
 * relying on fragile dynamic import("...") with runtime variables.
 *
 * Usage:  node apps/docs/scripts/generate-mdx-map.mjs
 */

import { readdirSync, statSync, writeFileSync } from "fs";
import { resolve, dirname, relative } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "../../..");
const CONTENT_DIR = resolve(ROOT, "apps/docs/@/content");
const OUTPUT_PATH = resolve(ROOT, "apps/docs/@/components/mdx-loaders.ts");

/* -------------------------------------------------------------------------- */
/*  Scan for .mdx files recursively                                          */
/* -------------------------------------------------------------------------- */

function scanMdxFiles(dir) {
  const entries = [];
  for (const name of readdirSync(dir)) {
    const fullPath = resolve(dir, name);
    if (statSync(fullPath).isDirectory()) {
      entries.push(...scanMdxFiles(fullPath));
    } else if (name.endsWith(".mdx")) {
      const rel = relative(CONTENT_DIR, fullPath).split("\\").join("/");
      // Key examples: "General/affix.en", "General/affix", "Business/approval-flow.zh"
      const key = rel.replace(/\.mdx$/, "");
      entries.push({ key, fullPath });
    }
  }
  return entries;
}

/* -------------------------------------------------------------------------- */
/*  Generate                                                                  */
/* -------------------------------------------------------------------------- */

function generate() {
  const files = scanMdxFiles(CONTENT_DIR);
  console.log(`Found ${files.length} MDX files`);

  // Deduplicate by key (shouldn't happen, but guard)
  const seen = new Set();
  const unique = [];
  for (const f of files) {
    if (seen.has(f.key)) continue;
    seen.add(f.key);
    unique.push(f);
  }

  // Sort by key for deterministic output order
  unique.sort((a, b) => a.key.localeCompare(b.key));

  const lines = [];
  lines.push("// Auto-generated by scripts/generate-mdx-map.mjs. Do not edit.");
  lines.push("// Static import map for all MDX documentation files.");
  lines.push("");
  lines.push("import type { ComponentType } from \"react\";");
  lines.push("");
  lines.push("export type MdxLoader = () => Promise<{ default: ComponentType }>;");
  lines.push("");
  lines.push("export const mdxLoaders: Record<string, MdxLoader> = {");

  for (const f of unique) {
    const importPath = `@/content/${f.key}.mdx`;
    lines.push(`  "${f.key}": () => import("${importPath}"),`);
  }

  lines.push("};");
  lines.push("");

  writeFileSync(OUTPUT_PATH, lines.join("\n"), "utf-8");
  console.log(`Generated ${OUTPUT_PATH}`);
  console.log(`  ${unique.length} MDX loader entries`);
}

generate();
